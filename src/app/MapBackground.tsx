const containerStyle = {
  width: "100%",
  height: "100%",
};

// I've set this to Kobe, Japan based on your school, but you can change it!
const center = {
  lat: 34.6996952,
  lng: 135.1927787,
};

const mapOptions = {
  styles: mapStyles,
  disableDefaultUI: true,
  zoomControl: false,
  mapTypeControl: false,
  streetViewControl: false,
  fullscreenControl: false,
};

function MapBackground() {
  const { isLoaded } = useJsApiLoader({
    id: "google-map-script",
    googleMapsApiKey: process.env.NEXT_PUBLIC_GOOGLE_MAPS_API_KEY || "",
  });

  if (!isLoaded) {
    // Fallback to a simple black background while the map loads or if the key is missing
    return <div className="fixed inset-0 bg-black z-0" />;
  }

  return (
    <div
      className="fixed inset-0 z-0 filter blur-[1px] scale-200"
      style={{
        maskImage: 'radial-gradient(ellipse at center, black 20%, transparent 80%)',
        WebkitMaskImage: 'radial-gradient(ellipse at center, black 20%, transparent 80%)'
      }}
    >
      <GoogleMap
        mapContainerStyle={containerStyle}
        center={center}
        zoom={14}
        options={mapOptions}
      />
    </div>
  );
}

export default React.memo(MapBackground);
